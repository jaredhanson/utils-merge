/**
 * Merge object b with object a.
 *
 *     var a = { foo: 'bar' }
 *       , b = { bar: 'baz' };
 *
 *     merge(a, b);
 *     // => { foo: 'bar', bar: 'baz' }
 *
 * @param {Object} a
 * @param {Object} b
 * @return {Object}
 * @api public
 */

exports = module.exports = function(a, b){
  if (a && b) {
    for (var key in b) {
     //fix prototype pollution
     if (key !== '__proto__' && key !== 'prototype' && key !== 'constructor'){
      a[key] = b[key];
     }
    }
  }
  return a;
};
